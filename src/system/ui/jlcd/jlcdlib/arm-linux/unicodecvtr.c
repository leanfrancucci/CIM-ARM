/**
 * Table: Unicode to LCD
 *        U+0080 - U+00FF
 */
static  EXMAPITEM exmap[] =
{
    {0x5C,0xDA}, /* \ */
    {0x7E,0xD5}, /* ~ */
    {0x7F,0xFF}, /* Del to Black */
    EXMAPITEM_END
};

#define UNICODE_START_TABLE     0x80
